    private void initCamera() throws IOException, NoPlayerException, CannotRealizeException{
    
        MediaLocator ml = new MediaLocator(" vfw://0");
        player =Manager.createRealizedPlayer(ml);
        this.jToggleButton1.add(player.getVisualComponent());
        player.start();
    
    }
    
    private void setImageButton(JToggleButton jbt,Image image){
    
        
        ImageIcon icon = new ImageIcon(new ImageIcon(image).getImage().getScaledInstance(95, 95, Image.SCALE_DEFAULT));
        
    jbt.setIcon(icon);
    }
