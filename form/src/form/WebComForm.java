    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            
            FrameGrabbingControl fgc = (FrameGrabbingControl)player.getControl("javax.media.control.FrameGrabbingControl");
            
            Buffer buffer=fgc.grabFrame();
            
            BufferToImage bti= new BufferToBufferedImage((VideoFormat)buffer.getFormat());
            
            Image image =bti.createImage(buffer);
            
            JIResizeImage resize = new JIResizeImage();
            
            bi = (BufferedImage)image;
            
            Image imageresize = resize.rescale(bi, 100, 100);
            
            this.setImageButton(this.jToggleButton2, imageresize);
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(WebComForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        FileDialog fd = new FileDialog(WebComForm.this,"save image",FileDialog.SAVE);
        
        fd.show();
        
        if(fd.getFile()!=null){
        
            nfile = fd.getDirectory()+fd.getFile()+".jpg";
        File make = new File(nfile);
        
        try{
        
        
            ImageIO.write(bi, "jpg",make);
            
        }catch(IOException e){
        
        
        }
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

